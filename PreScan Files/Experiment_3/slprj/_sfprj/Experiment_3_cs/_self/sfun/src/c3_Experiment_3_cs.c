static const mxArray *get_sim_state_c3_Experiment_3_cs
  (SFc3_Experiment_3_csInstanceStruct *chartInstance)
{
  const mxArray *c3_st;
  const mxArray *c3_y = NULL;
  int32_T c3_i1;
  real_T c3_u[2];
  const mxArray *c3_b_y = NULL;
  real_T c3_b_u[2];
  const mxArray *c3_c_y = NULL;
  real_T c3_c_u[4];
  const mxArray *c3_d_y = NULL;
  real_T c3_d_u[2];
  const mxArray *c3_e_y = NULL;
  real_T c3_e_u[2];
  const mxArray *c3_f_y = NULL;
  real_T c3_f_u[2];
  const mxArray *c3_g_y = NULL;
  real_T c3_g_u;
  const mxArray *c3_h_y = NULL;
  real_T c3_h_u[4];
  const mxArray *c3_i_y = NULL;
  uint8_T c3_i_u;
  const mxArray *c3_j_y = NULL;
  c3_st = NULL;
  c3_y = NULL;
  sf_mex_assign(&c3_y, sf_mex_createcellmatrix(9, 1), false);
  for (c3_i1 = 0; c3_i1 < 2; c3_i1++) {
    c3_u[c3_i1] = (*chartInstance->c3_Actor_traj_angle)[c3_i1];
  }

  c3_b_y = NULL;
  sf_mex_assign(&c3_b_y, sf_mex_create("y", c3_u, 0, 0U, 1U, 0U, 1, 2), false);
  sf_mex_setcell(c3_y, 0, c3_b_y);
  for (c3_i1 = 0; c3_i1 < 2; c3_i1++) {
    c3_b_u[c3_i1] = (*chartInstance->c3_Actor_traj_distance)[c3_i1];
  }

  c3_c_y = NULL;
  sf_mex_assign(&c3_c_y, sf_mex_create("y", c3_b_u, 0, 0U, 1U, 0U, 1, 2), false);
  sf_mex_setcell(c3_y, 1, c3_c_y);
  for (c3_i1 = 0; c3_i1 < 4; c3_i1++) {
    c3_c_u[c3_i1] = (*chartInstance->c3_pref)[c3_i1];
  }

  c3_d_y = NULL;
  sf_mex_assign(&c3_d_y, sf_mex_create("y", c3_c_u, 0, 0U, 1U, 0U, 1, 4), false);
  sf_mex_setcell(c3_y, 2, c3_d_y);
  for (c3_i1 = 0; c3_i1 < 2; c3_i1++) {
    c3_d_u[c3_i1] = chartInstance->c3_Actor_traj_anglePersist[c3_i1];
  }

  c3_e_y = NULL;
  if (!chartInstance->c3_Actor_traj_anglePersist_not_empty) {
    sf_mex_assign(&c3_e_y, sf_mex_create("y", NULL, 0, 0U, 1U, 0U, 2, 0, 0),
                  false);
  } else {
    sf_mex_assign(&c3_e_y, sf_mex_create("y", c3_d_u, 0, 0U, 1U, 0U, 2, 1, 2),
                  false);
  }

  sf_mex_setcell(c3_y, 3, c3_e_y);
  for (c3_i1 = 0; c3_i1 < 2; c3_i1++) {
    c3_e_u[c3_i1] = chartInstance->c3_Actor_traj_distancePersist[c3_i1];
  }

  c3_f_y = NULL;
  if (!chartInstance->c3_Actor_traj_distancePersist_not_empty) {
    sf_mex_assign(&c3_f_y, sf_mex_create("y", NULL, 0, 0U, 1U, 0U, 2, 0, 0),
                  false);
  } else {
    sf_mex_assign(&c3_f_y, sf_mex_create("y", c3_e_u, 0, 0U, 1U, 0U, 2, 1, 2),
                  false);
  }

  sf_mex_setcell(c3_y, 4, c3_f_y);
  for (c3_i1 = 0; c3_i1 < 2; c3_i1++) {
    c3_f_u[c3_i1] = chartInstance->c3_ActualTrajectoryIndex[c3_i1];
  }

  c3_g_y = NULL;
  if (!chartInstance->c3_ActualTrajectoryIndex_not_empty) {
    sf_mex_assign(&c3_g_y, sf_mex_create("y", NULL, 0, 0U, 1U, 0U, 2, 0, 0),
                  false);
  } else {
    sf_mex_assign(&c3_g_y, sf_mex_create("y", c3_f_u, 0, 0U, 1U, 0U, 2, 1, 2),
                  false);
  }

  sf_mex_setcell(c3_y, 5, c3_g_y);
  c3_g_u = chartInstance->c3_Counter;
  c3_h_y = NULL;
  if (!chartInstance->c3_Counter_not_empty) {
    sf_mex_assign(&c3_h_y, sf_mex_create("y", NULL, 0, 0U, 1U, 0U, 2, 0, 0),
                  false);
  } else {
    sf_mex_assign(&c3_h_y, sf_mex_create("y", &c3_g_u, 0, 0U, 0U, 0U, 0), false);
  }

  sf_mex_setcell(c3_y, 6, c3_h_y);
  for (c3_i1 = 0; c3_i1 < 4; c3_i1++) {
    c3_h_u[c3_i1] = chartInstance->c3_prefPersist[c3_i1];
  }

  c3_i_y = NULL;
  if (!chartInstance->c3_prefPersist_not_empty) {
    sf_mex_assign(&c3_i_y, sf_mex_create("y", NULL, 0, 0U, 1U, 0U, 2, 0, 0),
                  false);
  } else {
    sf_mex_assign(&c3_i_y, sf_mex_create("y", c3_h_u, 0, 0U, 1U, 0U, 2, 2, 2),
                  false);
  }

  sf_mex_setcell(c3_y, 7, c3_i_y);
  c3_i_u = chartInstance->c3_is_active_c3_Experiment_3_cs;
  c3_j_y = NULL;
  sf_mex_assign(&c3_j_y, sf_mex_create("y", &c3_i_u, 3, 0U, 0U, 0U, 0), false);
  sf_mex_setcell(c3_y, 8, c3_j_y);
  sf_mex_assign(&c3_st, c3_y, false);
  return c3_st;
}

static void set_sim_state_c3_Experiment_3_cs(SFc3_Experiment_3_csInstanceStruct *
  chartInstance, const mxArray *c3_st)
{
  const mxArray *c3_u;
  c3_u = sf_mex_dup(c3_st);
  c3_emlrt_marshallIn(chartInstance, sf_mex_dup(sf_mex_getcell(c3_u, 0)),
                      "Actor_traj_angle", *chartInstance->c3_Actor_traj_angle);
  c3_emlrt_marshallIn(chartInstance, sf_mex_dup(sf_mex_getcell(c3_u, 1)),
                      "Actor_traj_distance",
                      *chartInstance->c3_Actor_traj_distance);
  c3_c_emlrt_marshallIn(chartInstance, sf_mex_dup(sf_mex_getcell(c3_u, 2)),
                        "pref", *chartInstance->c3_pref);
  c3_e_emlrt_marshallIn(chartInstance, sf_mex_dup(sf_mex_getcell(c3_u, 3)),
                        "Actor_traj_anglePersist",
                        chartInstance->c3_Actor_traj_anglePersist);
  c3_g_emlrt_marshallIn(chartInstance, sf_mex_dup(sf_mex_getcell(c3_u, 4)),
                        "Actor_traj_distancePersist",
                        chartInstance->c3_Actor_traj_distancePersist);
  c3_i_emlrt_marshallIn(chartInstance, sf_mex_dup(sf_mex_getcell(c3_u, 5)),
                        "ActualTrajectoryIndex",
                        chartInstance->c3_ActualTrajectoryIndex);
  chartInstance->c3_Counter = c3_k_emlrt_marshallIn(chartInstance, sf_mex_dup
    (sf_mex_getcell(c3_u, 6)), "Counter");
  c3_m_emlrt_marshallIn(chartInstance, sf_mex_dup(sf_mex_getcell(c3_u, 7)),
                        "prefPersist", chartInstance->c3_prefPersist);
  chartInstance->c3_is_active_c3_Experiment_3_cs = c3_o_emlrt_marshallIn
    (chartInstance, sf_mex_dup(sf_mex_getcell(c3_u, 8)),
     "is_active_c3_Experiment_3_cs");
  sf_mex_destroy(&c3_u);
  sf_mex_destroy(&c3_st);
}

